/* -------------------------------------------------------------------------
 * Canvas alloc / free
 * ------------------------------------------------------------------------- */

Canvas canvas_new(int w, int h) {
    Canvas c;
    c.w = w; c.h = h;
    c.px = (uint8_t *)calloc((size_t)w * (size_t)h * 4, 1);
    return c;
}

void canvas_free(Canvas *c) {
    if (c && c->px) { free(c->px); c->px = NULL; }
}

/* -------------------------------------------------------------------------
 * Pixel helpers
 * ------------------------------------------------------------------------- */

/* Write one RGBA pixel; caller must ensure (x,y) is in bounds */
static inline void put_pixel(Canvas *c, int x, int y, uint32_t rgba) {
    uint8_t *p = c->px + ((size_t)y * (size_t)c->w + (size_t)x) * 4;
    p[0] = (uint8_t)(rgba >> 24);
    p[1] = (uint8_t)(rgba >> 16);
    p[2] = (uint8_t)(rgba >>  8);
    p[3] = (uint8_t)(rgba);
}

/* -------------------------------------------------------------------------
 * Drawing primitives
 * ------------------------------------------------------------------------- */

void ui_clear(Canvas *c, uint32_t rgba) {
    if (!c || !c->px) return;
    ui_fill_rect(c, 0, 0, c->w, c->h, rgba);
}

void ui_fill_rect(Canvas *c, int x, int y, int w, int h, uint32_t rgba) {
    if (!c || !c->px || w <= 0 || h <= 0) return;

    /* clip */
    int x0 = x < 0 ? 0 : x;
    int y0 = y < 0 ? 0 : y;
    int x1 = x + w; if (x1 > c->w) x1 = c->w;
    int y1 = y + h; if (y1 > c->h) y1 = c->h;
    if (x0 >= x1 || y0 >= y1) return;

    for (int py = y0; py < y1; py++)
        for (int px = x0; px < x1; px++)
            put_pixel(c, px, py, rgba);
}

void ui_rect(Canvas *c, int x, int y, int w, int h, uint32_t rgba) {
    if (!c || !c->px || w <= 0 || h <= 0) return;
    /* top / bottom rows */
    ui_fill_rect(c, x,         y,         w, 1, rgba);
    ui_fill_rect(c, x,         y + h - 1, w, 1, rgba);
    /* left / right columns (exclude corners already drawn) */
    ui_fill_rect(c, x,         y + 1, 1, h - 2, rgba);
    ui_fill_rect(c, x + w - 1, y + 1, 1, h - 2, rgba);
}

/* -------------------------------------------------------------------------
 * Text / bitmap font
 * ------------------------------------------------------------------------- */

/* Draw one glyph at (gx, gy); if bg_valid, fill cell with bg first */
static void draw_glyph(Canvas *c, int gx, int gy,
                        unsigned char ch, uint32_t fg,
                        int bg_valid, uint32_t bg) {
    if (ch < 0x20 || ch > 0x7E) ch = 0x20; /* unknown → space */

    const uint8_t *glyph = font8x8[ch - 0x20];

    for (int row = 0; row < 8; row++) {
        for (int col = 0; col < 8; col++) {
            int px = gx + col;
            int py = gy + row;
            if (px < 0 || py < 0 || px >= c->w || py >= c->h) continue;

            int bit = (glyph[row] >> col) & 1;
            if (bit)
                put_pixel(c, px, py, fg);
            else if (bg_valid)
                put_pixel(c, px, py, bg);
        }
    }
}

void ui_text(Canvas *c, int x, int y, const char *s, uint32_t fg) {
    if (!c || !c->px || !s) return;
    int cx = x, cy = y;
    for (; *s; s++) {
        if (*s == '\n') { cy += 8; cx = x; continue; }
        draw_glyph(c, cx, cy, (unsigned char)*s, fg, 0, 0);
        cx += 8;
    }
}

void ui_text_bg(Canvas *c, int x, int y, const char *s,
                uint32_t fg, uint32_t bg) {
    if (!c || !c->px || !s) return;
    int cx = x, cy = y;
    for (; *s; s++) {
        if (*s == '\n') { cy += 8; cx = x; continue; }
        draw_glyph(c, cx, cy, (unsigned char)*s, fg, 1, bg);
        cx += 8;
    }
}

/* -------------------------------------------------------------------------
 * GB framebuffer blit
 * ------------------------------------------------------------------------- */

void ui_blit_gb(Canvas *c, const uint8_t *fb, int x, int y, int scale,
                const uint32_t pal[4]) {
    if (!c || !c->px || !fb || !pal || scale <= 0) return;
    for (int sy = 0; sy < 144; sy++) {
        for (int sx = 0; sx < 160; sx++) {
            uint8_t shade = fb[sy * 160 + sx] & 3;
            uint32_t color = pal[shade];
            int bx = x + sx * scale;
            int by = y + sy * scale;
            ui_fill_rect(c, bx, by, scale, scale, color);
        }
    }
}

/* -------------------------------------------------------------------------
 * TextField widget
 * ------------------------------------------------------------------------- */

void textfield_clear(TextField *t) { t->text[0] = '\0'; t->len = 0; t->active = false; }

void textfield_putc(TextField *t, char ch) {
    if (ch < 0x20 || ch > 0x7E) return;          /* printable ASCII only */
    if (t->len >= TEXTFIELD_CAP - 1) return;
    t->text[t->len++] = ch;
    t->text[t->len] = '\0';
}

void textfield_backspace(TextField *t) {
    if (t->len > 0) t->text[--t->len] = '\0';
}

void textfield_render(Canvas *c, int x, int y, const TextField *t,
                      uint32_t fg, uint32_t bg) {
    ui_text_bg(c, x, y, t->text, fg, bg);
    if (t->active) ui_fill_rect(c, x + t->len * 8, y, 8, 8, fg);  /* caret block */
}

/* -------------------------------------------------------------------------
 * PNG save — inline RGBA (color type 6) PNG writer, zlib-compressed.
 * Technique matches src/shell/png.c / src/live/tile.c.
 * ------------------------------------------------------------------------- */

static void ui_put_be32(uint8_t *p, uint32_t v) {
    p[0] = (uint8_t)(v >> 24);
    p[1] = (uint8_t)(v >> 16);
    p[2] = (uint8_t)(v >>  8);
    p[3] = (uint8_t)(v);
}

static int ui_write_chunk(FILE *f, const char tag[4],
                           const uint8_t *data, uint32_t len) {
    uint8_t hdr[8];
    ui_put_be32(hdr, len);
    memcpy(hdr + 4, tag, 4);
    if (fwrite(hdr, 1, 8, f) != 8) return 1;
    if (len && fwrite(data, 1, len, f) != (size_t)len) return 1;
    uLong crc = crc32(0L, (const Bytef *)tag, 4);
    if (len) crc = crc32(crc, (const Bytef *)data, len);
    uint8_t crcb[4];
    ui_put_be32(crcb, (uint32_t)crc);
    return fwrite(crcb, 1, 4, f) != 4;
}

int ui_save_png(const Canvas *c, const char *path) {
    if (!c || !c->px || !path || c->w <= 0 || c->h <= 0) return 1;

    /* build raw scanlines: 1 filter byte (0) + w*4 RGBA bytes per row */
    size_t raw_len = (size_t)c->h * (1 + (size_t)c->w * 4);
    uint8_t *raw = (uint8_t *)malloc(raw_len);
    if (!raw) return 1;

    for (int row = 0; row < c->h; row++) {
        uint8_t *dst = raw + (size_t)row * (1 + (size_t)c->w * 4);
        dst[0] = 0; /* filter type None */
        memcpy(dst + 1, c->px + (size_t)row * (size_t)c->w * 4,
               (size_t)c->w * 4);
    }

    uLongf clen = compressBound((uLong)raw_len);
    uint8_t *comp = (uint8_t *)malloc(clen);
    if (!comp) { free(raw); return 1; }

    if (compress2(comp, &clen, raw, (uLong)raw_len, 9) != Z_OK) {
        free(raw); free(comp); return 1;
    }
    free(raw);

    FILE *f = fopen(path, "wb");
    if (!f) { free(comp); return 1; }

    static const uint8_t sig[8] = {137, 80, 78, 71, 13, 10, 26, 10};
    if (fwrite(sig, 1, 8, f) != 8) { free(comp); fclose(f); return 1; }

    uint8_t ihdr[13];
    ui_put_be32(ihdr,     (uint32_t)c->w);
    ui_put_be32(ihdr + 4, (uint32_t)c->h);
    ihdr[8]  = 8;  /* bit depth */
    ihdr[9]  = 6;  /* color type: RGBA */
    ihdr[10] = 0;  /* compression method */
    ihdr[11] = 0;  /* filter method */
    ihdr[12] = 0;  /* interlace method */

    if (ui_write_chunk(f, "IHDR", ihdr, 13)) { free(comp); fclose(f); return 1; }
    if (ui_write_chunk(f, "IDAT", comp, (uint32_t)clen)) { free(comp); fclose(f); return 1; }
    free(comp);
    if (ui_write_chunk(f, "IEND", NULL, 0)) { fclose(f); return 1; }
    fclose(f);
    return 0;
}
